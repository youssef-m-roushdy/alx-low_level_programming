#include <stdio.h>
#include <string.h>
/**
 * _strlen - Calculates the length of a string.
 * @s: Pointer to the input string.
 *
 * This function takes a pointer to
 * a null-terminated string @s and calculates
 * its length using the standard library function `strlen`.
 * It returns the length
 * of the string.
 *
 * Return: Length of the string.
 */
int _strlen(char *s)
{
return (strlen(s));
}
