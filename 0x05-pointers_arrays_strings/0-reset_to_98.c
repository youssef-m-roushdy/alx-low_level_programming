#include <stdio.h>
/**
 * reset_to_98 - Resets an integer pointed to by a pointer to the value 98.
 * @n: Pointer to an integer variable.
 *
 * This function modifies the value stored at the memory location pointed
 * to by the integer pointer @n and sets it to 98.
 */
void reset_to_98(int *n)
{
*n = 98;
}
